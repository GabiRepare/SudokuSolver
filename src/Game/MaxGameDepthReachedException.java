package Game;

public class MaxGameDepthReachedException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public MaxGameDepthReachedException() {
		super();
	}

	public MaxGameDepthReachedException(String arg0) {
		super(arg0);
	}

	public MaxGameDepthReachedException(Throwable arg0) {
		super(arg0);
	}

	public MaxGameDepthReachedException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public MaxGameDepthReachedException(String arg0, Throwable arg1,
			boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
